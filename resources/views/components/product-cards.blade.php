<script>
function toggleDropdown() {
    const dropdown = document.getElementById('dropdown');
    dropdown.style.display = dropdown.style.display === 'none' ? 'block' : 'none';
}

function selectOption(element, value) {
    document.getElementById('selectedOption').textContent = value;
    const options = document.getElementsByClassName('dropdown-option');
    for (let option of options) {
        option.style.backgroundColor = 'white';
        option.style.color = 'black';
        const checkIcon = option.querySelector('.fa-check');
        if (checkIcon) {
            checkIcon.remove();
        }
        if (!option.querySelector('span')) {
            option.insertAdjacentHTML('afterbegin', '<span style="width: 14px; margin-right: 8px;"></span>');
        }
    }
    element.style.backgroundColor = '#dc3545';
    element.style.color = 'white';
    const span = element.querySelector('span');
    if (span) {
        span.outerHTML = '<i class="fas fa-check" style="font-size: 14px; margin-right: 8px; width: 14px;"></i>';
    }
    toggleDropdown();
}

// Close dropdown when clicking outside
document.addEventListener('click', function(event) {
    const dropdown = document.getElementById('dropdown');
    const customSelect = event.target.closest('.custom-select');
    if (!customSelect && dropdown.style.display === 'block') {
        dropdown.style.display = 'none';
    }
});
</script> 